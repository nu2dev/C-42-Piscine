int		ft_iterative_factorial(int nb)
{
	int i;
	int n;

	i = 0;
	n = 1;
	if (nb < 0)
		return (0);
	while (i < nb)
	{
		n = n * (i + 1);
		i++;
	}
	return (n);
}
