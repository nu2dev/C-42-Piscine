char	*ft_strlowcase(char *str)
{
	int idx;

	idx = -1;
	while (str[++idx] != '\0')
	{
		if ((str[idx] >= 'A' && str[idx] <= 'Z'))
			str[idx] -= 'A' - 'a';
	}
	return (str);
}
