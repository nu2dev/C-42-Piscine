#define MAX_SQRT 46340

int		ft_sqrt(int nb)
{
	int i;
	int sqr;

	if (nb < 0)
		return (0);
	i = 0;
	while (((sqr = i * i) <= nb) && i <= MAX_SQRT)
	{
		if (sqr == nb)
			return (i);
		i++;
	}
	return (0);
}
