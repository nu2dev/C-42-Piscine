#define MAX_SQRT 46341

int		ft_is_prime(int nb)
{
	int i;

	if (nb < 2)
		return (0);
	i = 2;
	while (i * i <= nb && i <= MAX_SQRT)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
