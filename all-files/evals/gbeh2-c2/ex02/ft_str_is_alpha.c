int	ft_str_is_alpha(char *str)
{
	int idx;

	idx = -1;
	while (str[++idx] != '\0')
	{
		if (!(
			(str[idx] >= 'a' && str[idx] <= 'z') ||
			(str[idx] >= 'A' && str[idx] <= 'Z')))
			return (0);
	}
	return (1);
}
