char	*ft_strupcase(char *str)
{
	char	*org_str;

	org_str = str;
	while (*str != '\0')
	{
		if (*str >= 'a' && *str <= 'z')
			*str -= ('a' - 'A');
		str++;
	}
	return (org_str);
}
